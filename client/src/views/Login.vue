<template>
  <div class="rounded-t-lg overflow-hidden px-3 py-10 bg-gray-200 flex justify-center">
    <div class="w-full max-w-xs">
      <form class="bg-white shadow-md rounded px-8 pt-6 pb-8 mb-4">
        <div class="mb-4">
          <label class="block text-gray-700 text-sm font-bold mb-2" for="username">Username</label>
          <input
            id="username"
            type="text"
            class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 leading-tight focus:outline-none focus:shadow-outline"
            :class="{ 'border-red-500 mb-3': $v.username.$error }"
            required
            placeholder="Username"
            autocomplete="username"
            autofocus
            v-model="username"
          />
          <p v-if="$v.username.$error" class="text-red-500 text-xs italic">Enter your username.</p>
        </div>
        <div class="mb-6">
          <label class="block text-gray-700 text-sm font-bold mb-2" for="password">Password</label>
          <input
            id="password"
            type="password"
            class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 leading-tight focus:outline-none focus:shadow-outline"
            :class="{ 'border-red-500 mb-3': $v.password.$error }"
            required
            placeholder="******************"
            autocomplete="current-password"
            v-model="password"
          />
          <p v-if="$v.password.$error" class="text-red-500 text-xs italic">Enter your password.</p>
        </div>
        <div class="flex items-center justify-between">
          <button type="submit" class="btn btn-blue" @click.prevent="onSubmit">Login</button>
          <!-- <a
            class="inline-block align-baseline font-bold text-sm text-blue-500 hover:text-blue-800"
            href="#"
          >
            Forgot Password?
          </a>-->
        </div>
      </form>
    </div>
  </div>
</template>

<script>
import { required, email } from "vuelidate/lib/validators";

export default {
  data() {
    return {
      username: "",
      password: ""
    };
  },
  methods: {
    async onSubmit() {
      this.$v.$touch();

      if (this.$v.username.$error || this.$v.password.$error) return;

      this.$store.dispatch("auth/login", {
        username: this.username,
        password: this.password
      });
    }
  },
  validations: {
    username: {
      required,
      email
    },
    password: {
      required
    }
  }
};
</script>
